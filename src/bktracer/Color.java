public class Color {
    private int r, g, b;

    public int getR(){
        return r;
    }

    public int getG(){
        return g;
    }

    public int getB(){
        return b;
    }

    public Color(){
    }

    public Color(int r, int g, int b){
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
